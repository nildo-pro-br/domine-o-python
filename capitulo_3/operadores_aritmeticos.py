#
# Operações com números e variáveis
#
# Variáveis para as operações
a = 10
b = 3

# Adição
soma = a + b
print(f"Soma: {soma}") # Saída: Soma: 13

# Subtração
subtracao = a - b
print(f"Subtração: {subtracao}") # Saída: Subtração: 7

# Multiplicação
multiplicacao = a * b
print(f"Multiplicação: {multiplicacao}") # Saída: Multiplicação: 30

# Divisão (sempre float)
divisao = a / b
print(f"Divisão: {divisao}") # Saída: Divisão: 3.3333333333333335

# Divisão Inteira (apenas a parte inteira)
divisao_inteiros = a // b
print(f"Divisão inteiros: {divisao_inteiros}") # Saída: Divisão Inteira: 3

# Exponenciação (potência)
resto = a % b
print(f"Resto da divisão: {resto}") # Saída: Resto da Divisão: 1 (10 dividido por 3 é 3, com resto 1)

# Exponenciação (potência)
potencia = a ** b # 10 elevado a 2
print(f"Potência: {potencia}") # Saída: Potência (10^3): 1000

print(f"2 elevado a 3 é: {2 ** 3}") # Saída: 2 elevado a 3 é: 8
