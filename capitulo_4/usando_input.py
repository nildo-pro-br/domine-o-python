# Solicitando o nome do usuário e armazenando a resposta na variável 'seu_nome'
seu_nome = input("Qual é o seu nme, usuário? ")
print(f"É um prazer conhecer você {seu_nome}!")

# Solicitando a idade (o valor será lido como texto)
sua_idade_texto = input("Quantos anos você tem? ")
print(f"Então você tem {sua_idade_texto} anos. Que interessante!")

# Exemplo de interação simples
comida_favorita = input("Qual a sua comida favorita? ")
print(f"Ah, {comida_favorita} é uma delícia")

