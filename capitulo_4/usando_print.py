#
# Função print()
#
# Exibindo uma string literal
print("Eu aprecio programar em Python!")

# Exibindo o valor de uma variável numérica
idade_do_robo = 5
print(idade_do_robo) # Saída: 5

# Exibindo o resultado de uma operação aritmética
print(10 + 20) # Saída: 30

# Exibindo o conteúdo de uma variável textual com concatenação
nome = "Mila"
print("Olá, " + nome + "! Tudo bem?") # Saída: Olá, Mila! Tudo bem? (Exemplo de concatenação de strings)

# Exibindo múltiplos itens separados por vírgula (Python insere espaços automaticamente)
print("Meu nome é", nome, "e tenho", idade_do_robo, "anos.") # Saída: Meu nome é Mila e tenho 5 anos.
print() # pula uma linha
