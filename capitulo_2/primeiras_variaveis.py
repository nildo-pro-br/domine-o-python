#
# Criação de variáveis e a atribuição de diferentes tipos de valores
#
# Atribuindo um valor numérico à variável 'idade'
idade = 30
print(idade) # Saída: 30

# Atribuindo um valor textual à variável 'nome'
nome = "Alice"
print(nome) # Saída : alice

# Atribuindo o resultado de uma operação à variável 'soma'
numero1 = 10
numero2 = 5
soma = numero1 + numero2
print(soma) # Saída: 15 (o resultado da soma)

# Reatribuindo um valor a uma variável existente
minha_cor_favorita = "Azul"
print(minha_cor_favorita) # Saída: Azul

minha_cor_favorita = "Verde" # O valor anterior é substituído
print(minha_cor_favorita) # Saída: Verde


