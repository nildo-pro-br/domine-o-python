#
# Calculadora de Custos de Viagem
#
# Variáveis para armazenar os custos da viagem
passagem_aerea = 750.50     # Custo da passagem (tipo float)
diarias_hotel = 2 * 120.00  # Custo de 2 diárias de hotel (tipo float)
gastos_comida = 300.00      # Estimativa de gastos com alimentação (tipo float)
passeios = 150.00           # Custos com atividades de lazer (tipo floa

# Cálculo do custo total da viagem
total_viagem = passagem_aerea + diarias_hotel + gastos_comida + passeios

# Exibição dos resultados
print("----- Resumo da Viagem -----")
print(f"Passagem: R${passagem_aerea}")
print(f"hotel: R${diarias_hotel}")
print(f"Comida: R${gastos_comida}")
print(f"Passeios: R${passeios}")
print("----------------------------")
print(f"Total da Viagem: R${total_viagem}")
print("Total da Viagem: R${total_viagem}")
