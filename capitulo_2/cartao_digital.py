# Geração de Cartão de Visitas Digital
#
# Criar um cartão de visitas básico atribuindo cada dado relevante a uma
# variável específica.
#
# Definição de variáveis para o cartão de visitas
nome = "Sr. Coelhinho"
profissao = "Programador de Cenouras"
email = "coelhinho@python.com"
telefone = "(99) 99999-9999"
idade = 3 # Idade em unidade de tempo

# Exibição do cartão de visitas
print("--- Cartão de Visitas ---")
print(f"Nome: {nome}")
print(f"Profissão: {profissao}")
print(f"Email: {email}")
print(f"Telefone: {telefone}")
print(f"Idade: {idade} anos")
print("-------------------------")

