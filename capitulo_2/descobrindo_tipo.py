#
# Verificando o tipo de dado com a função type()
#
minha_idade = 51
meu_nome = "Robô Aprende Tudo"
minha_altura = 1.70
tem_internet = True

print(type(minha_idade))      # Saída: <class 'int'>
print(type(meu_nome))         # Saída: <class 'str'>
print(type(minha_altura))     # Saída: <class 'float'>
print(type(tem_internet))     # Saída: <class 'bool'>
